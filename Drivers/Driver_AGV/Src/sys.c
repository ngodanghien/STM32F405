/**
  * @brief  Error_DriverHandler
  * Chú ý: Cần phải Init GPIO trước khi sử dụng.
  * @param  void
  * @retval void : Nháy LED liên tục 500ms
  */
void Error_DriverHandler(void)
{
	while(1)
	{
		GPIOB->ODR ^= USER_LED_Pin;
		delay_ms(500);
	}
}
